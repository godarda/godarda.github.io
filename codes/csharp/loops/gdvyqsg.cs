Console.WriteLine("———————————————————————————————————————————");
Console.WriteLine("Program to print the even and odd numbers");
Console.WriteLine("———————————————————————————————————————————");
Console.WriteLine("Even numbers are:");
for (int i = 0; i <= 15; i++)
{
    if (i % 2 == 0)
    {
        Console.WriteLine(i);
    }
}
Console.WriteLine("———————————————————————————————————————————");
Console.WriteLine("Odd numbers are:");
for (int i = 0; i <= 15; i++)
{
    if (i % 2 == 1)
    {
        Console.WriteLine(i);
    }
}
Console.WriteLine("———————————————————————————————————————————");
